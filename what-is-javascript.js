// JavaScript Nedir ve Nasıl Çalışır?
// ==================================
// JavaScript, web sayfalarını interaktif hale getirmek için kullanılan yüksek seviyeli,
// yorumlanabilir bir programlama dilidir.

// 1. JavaScript Nedir?
// -------------------
// - Web tarayıcılarında çalışan bir script dilidir
// - HTML ve CSS ile birlikte web'in temel teknolojilerinden biridir
// - Hem istemci (client-side) hem de sunucu (server-side, Node.js ile) tarafında çalışabilir
// - Dinamik içerik oluşturmak, kullanıcı etkileşimleri yönetmek için kullanılır

// 2. JavaScript Ne İçin Kullanılır?
// --------------------------------
// - Web sayfalarını interaktif hale getirmek
// - Form doğrulaması yapmak
// - Animasyonlar oluşturmak
// - API'lerden veri çekmek
// - Tek sayfa uygulamaları (SPA) geliştirmek
// - Mobil uygulamalar geliştirmek (React Native gibi)
// - Oyunlar geliştirmek
// - Sunucu taraflı uygulamalar geliştirmek (Node.js ile)

// 3. JavaScript Nasıl Çalışır?
// ---------------------------
// - Tarayıcıda JavaScript motoru tarafından yorumlanır (V8, SpiderMonkey gibi)
// - HTML sayfası yüklendiğinde, tarayıcı JavaScript kodunu satır satır okur ve çalıştırır
// - DOM (Document Object Model) aracılığıyla HTML elemanlarına erişir ve değiştirebilir
// - Asenkron işlemler yapabilir (AJAX, Promises, async/await)
