// Declare a variable x
let x;

// Get the value of PI from Math object
x = Math.PI;
console.log(x);

// Round 4.6 to the nearest integer (5)
x = Math.round(4.6);
console.log(x);

// Get the absolute value of -4.6 (4.6)
x = Math.abs(-4.6);
console.log(x);

// Round 4.1 up to the nearest integer (5)
x = Math.ceil(4.1);
console.log(x);

// Round 4.9 down to the nearest integer (4)
x = Math.floor(4.9);
console.log(x);

// Get the square root of 64 (8)
x = Math.sqrt(64);
console.log(x);

// Get the maximum value from the given numbers
x = Math.max(23, 34, 55, 88, 99, 100);
console.log(x);

// Get the minimum value from the given numbers
x = Math.min(23, 34, 55, 88, 99, 100);
console.log(x);

// Get a random number between 0 and 1
x = Math.random();
console.log(x);

// Get the power of 2 to the power of 3 (8)
x = Math.pow(2, 3);
console.log(x);
