// Arithmetic Operators

let x;

// Addition
x = 5 + 5;
console.log(x);

// Concatenation
x = 'Hello' + ' World';
console.log(x);

// Subtraction
x = 5 - 5;
console.log(x);

// Multiplication
x = 5 * 5;
console.log(x);

// Division
x = 5 / 5;
console.log(x);

// Modulus
x = 5 % 5;
console.log(x);

// Exponentiation
x = 5 ** 5;
console.log(x);

// Increment
x = 5;
x++;
console.log(x);

// Decrement
x = 5;
x--;
console.log(x);

// Comparison Operators

// Equal
x = 5 == 5;
console.log(x);

// Strict Equal
x = 5 === '5';
console.log(x);

// Not Equal
x = 5 != 5;
console.log(x);

// Strict Not Equal
x = 5 !== '5';
console.log(x);

// Greater Than
x = 5 > 5;
console.log(x);

// Less Than
x = 5 < 5;
console.log(x);

// Greater Than or Equal To
x = 5 >= 5;
console.log(x);

// Less Than or Equal To
x = 5 <= 5;
console.log(x);

// Logical Operators

// AND
x = 5 && 5;
console.log(x);

// OR
x = 5 || 5;
console.log(x);

// NOT
x = !5;
console.log(x);

